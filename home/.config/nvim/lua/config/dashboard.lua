---@return string headers random header from the table
M.get_random_header = function()
	-- Pick a random header from the table
	local keys = vim.tbl_keys(M.headers)
	local random_key = keys[math.random(#keys)]
	return M.headers[random_key]
end

M.get_header = function(h)
	local keys = vim.tbl_keys(M.headers)

	-- Sort numerically by the number in "headerX"
	table.sort(keys, function(a, b)
		local na = tonumber(a:match("header(%d+)"))
		local nb = tonumber(b:match("header(%d+)"))
		return na < nb
	end)

	return M.headers[keys[h]]
end

return M
